function readTranslation(tree: TranslationTree, key: string): string | undefined {
  let current: string | TranslationTree | undefined = tree;

  for (const segment of key.split(".")) {
    if (!current || typeof current === "string") {
      return undefined;
    }

    current = current[segment];
  }

  return typeof current === "string" ? current : undefined;
}

export function isSupportedLocale(value: unknown): value is AppLocale {
  return typeof value === "string" && supportedLocales.includes(value as AppLocale);
}

export function normalizeLocale(value: unknown): AppLocale {
  if (typeof value !== "string") {
    return DEFAULT_LOCALE;
  }

  const normalizedValue = value.trim().toLowerCase().replace("_", "-");
  const primaryLanguage = normalizedValue.split("-")[0];

  if (isSupportedLocale(normalizedValue)) {
    return normalizedValue;
  }

  if (isSupportedLocale(primaryLanguage)) {
    return primaryLanguage;
  }

  return DEFAULT_LOCALE;
}

export function getLocaleLabel(locale: AppLocale) {
  return localeLabels[locale];
}

export function getDateFnsLocale(locale: AppLocale) {
  return dateFnsLocales[locale];
}

export function t(
  localeInput: unknown,
  key: string,
  replacements: Record<string, string | number> = {},
) {
  const locale = normalizeLocale(localeInput);
  const value =
    readTranslation(translations[locale], key) ??
    readTranslation(translations[DEFAULT_LOCALE], key) ??
    key;

  return Object.entries(replacements).reduce(
    (result, [placeholder, replacement]) =>
      result.replaceAll(`{${placeholder}}`, String(replacement)),
    value,
  );
}

export function getSupportedLocaleOptions() {
  return supportedLocales.map((locale) => ({
    value: locale,
    label: getLocaleLabel(locale),
  }));
}

