export function formatAppointmentStatus(status: string, localeInput: unknown) {
  if (status === "PENDING") {
    return t(localeInput, "common.statusPending");
  }

  if (status === "CONFIRMED") {
    return t(localeInput, "common.statusConfirmed");
  }

  if (status === "CANCELLED") {
    return t(localeInput, "common.statusCancelled");
  }

  if (status === "COMPLETED") {
    return t(localeInput, "common.statusCompleted");
  }

  return status;
}

